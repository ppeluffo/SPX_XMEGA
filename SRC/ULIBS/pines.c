{
	CONFIG_RELE_K2();
	CLEAR_RELE_K2();
}

// -----------------------------------------------------------------------------
void VSENSORS420_init(void)
{
    // Configura el pin del SENSORS420 como output
	CONFIG_VSENSORS420();
	CLEAR_VSENSORS420();
}

// -----------------------------------------------------------------------------
uint8_t FC1_read(void)
{
   return ( ((FC1_PORT.IN) >> FC1 ) & 0x01 );
}
// -----------------------------------------------------------------------------
uint8_t FC2_read(void)
{
   return ( ((FC2_PORT.IN) >> FC2 ) & 0x01 );
}
// -----------------------------------------------------------------------------
void FCx_init(void)
{
    CONFIG_FC1();
    CONFIG_FC2();
}
